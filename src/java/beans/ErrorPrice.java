package beans;

/**
 *
 * @author Admin
 */
public class ErrorPrice {
    String nameError, informationError, durationError, salepriceError, listpriceError;

    public ErrorPrice() {
    }

    public String getNameError() {
        return nameError;
    }

    public void setNameError(String nameError) {
        this.nameError = nameError;
    }

    public String getInformationError() {
        return informationError;
    }

    public void setInformationError(String informationError) {
        this.informationError = informationError;
    }

    public String getDurationError() {
        return durationError;
    }

    public void setDurationError(String durationError) {
        this.durationError = durationError;
    }

    public String getSalepriceError() {
        return salepriceError;
    }

    public void setSalepriceError(String salepriceError) {
        this.salepriceError = salepriceError;
    }

    public String getListpriceError() {
        return listpriceError;
    }

    public void setListpriceError(String listpriceError) {
        this.listpriceError = listpriceError;
    }


    
    
}
